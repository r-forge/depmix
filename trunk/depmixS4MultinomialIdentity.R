inCov <- transInit(~age,ns=nstates,ps=instart,data=data.frame(age=age),fixed=infix,family=multinomial(base=1))
inmod <- transInit(~1,ns=nstates,ps=instart[1:nstates],data=data.frame(rep(1,length(lengths))),fixed=infix[1:nstates])

