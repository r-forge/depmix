# nine state model = mixture of  all-or-none , ci, and guessing model
# covariate age on the initial probabilities
nn<-.000000000001
rModels <- list(
        list(GLMresponse(formula=ndat~1,family=multinomial(),pstart=c(nn,1),fixed=c(T,T))),       #init AN
        list(GLMresponse(formula=ndat~1,family=multinomial(),pstart=c(nn,1),fixed=c(T,T))),       #init CI
        list(GLMresponse(formula=ndat~1,family=multinomial(),pstart=c(nn,1),fixed=c(T,T))),       #init G
        list(GLMresponse(formula=ndat~1,family=multinomial(),pstart=c(0.05,0.95),fixed=c(T,F))), #AN L
        list(GLMresponse(formula=ndat~1,family=multinomial(),pstart=c(0.5,0.5),fixed=c(T,F))),   #AN g
        list(GLMresponse(formula=ndat~1,family=multinomial(),pstart=c(.05,.95),fixed=c(T,F))),   #CI L
        list(GLMresponse(formula=ndat~1,family=multinomial(),pstart=c(nn,1),fixed=c(T,T))),       #CI C
        list(GLMresponse(formula=ndat~1,family=multinomial(),pstart=c(1,nn),fixed=c(T,T))),       #CI E
        list(GLMresponse(formula=ndat~1,family=multinomial(),pstart=c(0.5,0.5),fixed=c(T,T)))   # G
)
trstart=c(
rep(nn,4),1,rep(nn,4),
rep(nn,5),.02,1-.5-.02,.5,nn,
rep(nn,8),1,
rep(nn,3),1,rep(nn,5),
rep(nn,3),.04,1-.04,rep(nn,4),
rep(nn,5),1,rep(nn,3),
rep(nn,5),nn,3/8,5/8,nn,
rep(nn,5),.4,(1-.4)*3/8,(1-.4)*5/8,nn,
rep(nn,8),1)
trfix<-c(
rep(T,4),T,rep(T,4),
rep(T,5),F,F,F,T,
rep(T,9),
rep(T,3),T,rep(T,5),
rep(T,3),F,F,rep(T,4),
rep(T,5),T,rep(T,3),
rep(T,5),T,T,T,T,
rep(T,5),F,F,F,T,
rep(T,8),T)

nstates=9
transition <- list()
for (i in 0:(nstates-1)){
tmp<-trfix[i*9+(1:9)]
if (all(tmp)) {base=1
}else {base=min(which(trfix[i*9+(1:9)]==F));tmp[min(which(trfix[i*9+(1:9)]==F))]<-T;}
transition[[i+1]] <- transInit(~1,nstates=9,data=data.frame(1),pstart=c(trstart[i*9+(1:9)]),family=multinomial(base=base),fixed=tmp)
}
